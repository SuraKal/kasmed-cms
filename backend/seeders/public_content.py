def seed_public_content(
    db,
    engagement_model,
    solution_model,
    value_model,
    partner_model,
    gallery_model,
    update_existing=False,
):
    counts = {"created": 0, "updated": 0, "unchanged": 0}

    for sort_order, values in enumerate(ENGAGEMENTS, start=1):
        _seed_item(
            db,
            engagement_model,
            {"slug": values["slug"]},
            {**values, "status": "active", "sort_order": sort_order},
            update_existing,
            counts,
        )

    for sort_order, values in enumerate(SOLUTIONS, start=1):
        _seed_item(
            db,
            solution_model,
            {"slug": values["slug"]},
            {
                "featured": False,
                **values,
                "status": "active",
                "sort_order": sort_order,
            },
            update_existing,
            counts,
        )

    for sort_order, values in enumerate(CORE_VALUES, start=1):
        _seed_item(
            db,
            value_model,
            {"slug": values["slug"]},
            {**values, "status": "active", "sort_order": sort_order},
            update_existing,
            counts,
        )

    for sort_order in range(1, 18):
        _seed_partner(
            db,
            partner_model,
            "suppliers",
            f"supplier-{sort_order:02d}",
            f"Supplier {sort_order:02d}",
            f"/images/suppliers/{sort_order}.png",
            sort_order,
            update_existing,
            counts,
        )

    for sort_order, name in enumerate(CLIENT_NAMES, start=1):
        _seed_partner(
            db,
            partner_model,
            "clients",
            _simple_slug(name),
            name,
            f"/images/clients/{sort_order}.png",
            sort_order,
            update_existing,
            counts,
        )

    for sort_order in range(1, 15):
        _seed_partner(
            db,
            partner_model,
            "customers",
            f"customer-{sort_order:02d}",
            f"Customer {sort_order:02d}",
            f"/images/customers/{sort_order}.png",
            sort_order,
            update_existing,
            counts,
        )

    for sort_order, values in enumerate(GALLERY, start=1):
        _seed_item(
            db,
            gallery_model,
            {"title": values["title"]},
            {
                **values,
                "caption": "",
                "status": "active",
                "sort_order": sort_order,
            },
            update_existing,
            counts,
        )

    db.session.commit()
    return counts


def _seed_partner(
    db,
    model,
    partner_type,
    slug,
    name,
    logo,
    sort_order,
    update_existing,
    counts,
):
    _seed_item(
        db,
        model,
        {"partner_type": partner_type, "slug": slug},
        {
            "partner_type": partner_type,
            "slug": slug,
            "name": name,
            "logo": logo,
            "website": "",
            "status": "active",
            "sort_order": sort_order,
        },
        update_existing,
        counts,
    )


def _seed_item(db, model, lookup, values, update_existing, counts):
    item = model.query.filter_by(**lookup).first()
    if item is None:
        db.session.add(model(**values))
        counts["created"] += 1
        return

    if not update_existing:
        counts["unchanged"] += 1
        return

    for field, value in values.items():
        setattr(item, field, value)
    counts["updated"] += 1


def _simple_slug(value):
    return (
        value.lower()
        .replace("&", "and")
        .replace(" ", "-")
        .replace("/", "-")
        .replace(".", "")
    )
